#include "main.h"

/**
 * add - add two integers
 * @a: input param one
 * @b: input param two
 *
 * Return: final result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two integers
 * @a: input param 1
 * @b: input param 2
 *
 * Return: final result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two integers
 * @a: input param 1
 * @b: input param 2
 *
 * Return: final result
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two integers
 * @a: input param 1
 * @b: input param 2
 *
 * Return: final result
 */
int div_digit(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod of two integers
 * @a: input param 1
 * @b: input param 2
 *
 * Return: final result
 */
int mod(int a, int b)
{
	return (a % b);
}
